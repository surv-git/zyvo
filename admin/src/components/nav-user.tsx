const handleLogout = async () => {
  try {
    // Clear authentication tokens from localStorage
    localStorage.removeItem('auth_token');
    localStorage.removeItem('refresh_token');
    localStorage.removeItem('user_data');
    
    // Clear any session storage
    sessionStorage.clear();
    
    // Clear theme preferences if needed (optional)
    // localStorage.removeItem('theme');
    // localStorage.removeItem('active_theme');
    
    // Make API call to logout endpoint (if available)
    try {
      await fetch('/api/v1/auth/logout', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'Authorization': `Bearer ${localStorage.getItem('auth_token') || ''}`,
        },
      });
    } catch (apiError) {
      // API logout failed, but we'll still proceed with client-side logout
      console.warn('API logout failed:', apiError);
    }
    
    // Show success message (you can replace with toast if available)
    console.log('Logged out successfully');
    
    // Redirect to login page
    window.location.href = '/login';
    
  } catch (error) {
    console.error('Logout error:', error);
    // Even if there's an error, redirect to login for security
    window.location.href = '/login';
  }
};

