/**
 * Find the best matching image for a category name
 */
function findBestImageForCategory(categoryName) {
  const name = categoryName.toLowerCase();
  
  // Direct match
  if (CATEGORY_IMAGE_MAPPINGS[name]) {
    return CATEGORY_IMAGE_MAPPINGS[name];
  }
  
  // Partial match
  for (const [key, url] of Object.entries(CATEGORY_IMAGE_MAPPINGS)) {
    if (name.includes(key) || key.includes(name)) {
      return url;
    }
  }
  
  // Default fallback
  return CATEGORY_IMAGE_MAPPINGS.default;
}

/**
 * Check if URL is valid
 */
function isValidImageUrl(url) {
  if (!url || typeof url !== 'string') return false;
  
  try {
    const urlObj = new URL(url);
    return urlObj.protocol === 'http:' || urlObj.protocol === 'https:';
  } catch {
    return false;
  }
}

/**
 * Main function to update category images
 */
async function updateCategoryImages() {
  try {
    console.log('üîÑ Connecting to MongoDB...');
    await mongoose.connect(process.env.MONGODB_URI);
    console.log('‚úÖ Connected to MongoDB');

    // Get all categories
    console.log('üìã Fetching all categories...');
    const categories = await Category.find({});
    console.log(`üìä Found ${categories.length} categories`);

    let updatedCount = 0;
    let skippedCount = 0;

    for (const category of categories) {
      // Find best matching image for this category
      const newImageUrl = findBestImageForCategory(category.name);
      const oldImageUrl = category.image_url;
      
      // Update category with new Unsplash image
      await Category.findByIdAndUpdate(category._id, {
        image_url: newImageUrl,
        updatedAt: new Date()
      });

      console.log(`‚úÖ Updated "${category.name}"`);
      console.log(`   Old: ${oldImageUrl || 'No image'}`);
      console.log(`   New: ${newImageUrl}`);
      console.log('');
      updatedCount++;
    }

    console.log('\nüìä Summary:');
    console.log(`‚úÖ Updated: ${updatedCount} categories`);
    console.log(`‚è≠Ô∏è  Skipped: ${skippedCount} categories (already had valid images)`);
    console.log(`üìã Total: ${categories.length} categories processed`);

    if (updatedCount > 0) {
      console.log('\nüéâ Category images updated successfully!');
    } else {
      console.log('\n‚ú® All categories already have valid images!');
    }

  } catch (error) {
    console.error('‚ùå Error updating category images:', error);
    process.exit(1);
  } finally {
    await mongoose.disconnect();
    console.log('üîå Disconnected from MongoDB');
  }
}

